export function getButikkerByKommune(kommune: string): Butikk[] {
  return butikker.filter(b => b.kommune.toLowerCase() === kommune.toLowerCase());
}

export function getAllKommuner(): { kommune: string; antall: number }[] {
  const map = new Map<string, number>();
  butikker.forEach(b => map.set(b.kommune, (map.get(b.kommune) || 0) + 1));
  return Array.from(map.entries())
    .map(([kommune, antall]) => ({ kommune, antall }))
    .sort((a, b) => b.antall - a.antall);
}
